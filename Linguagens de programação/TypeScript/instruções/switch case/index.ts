// ==> switch case
const flor: string = 'tulipa'

switch (flor) {
    case 'rosa':
        console.log('Rosas são vermelhas')
        break
    case 'violeta':
        console.log('Violetas são azuis')
        break
    case 'tulipa':
        console.log('Tulipas são brancas')
        break
    default: 
        console.log('Por favor, selecione uma flor!')
}