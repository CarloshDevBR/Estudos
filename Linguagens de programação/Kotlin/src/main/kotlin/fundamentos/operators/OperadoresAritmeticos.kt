package fundamentos.operators/*
    + Operador de Soma
    - Operador de Subtração
    / Operador de Divisão
    * Operador de Multiplicação
    % Módulo - Resto da Divisão

    ++ Incremento
    -- Decremento
*/

fun main() {
    var n1: Int = 20
    var n2: Int = 10

    // Operador de Soma
    println(n1 + n2)

    // Operador de Subtração
    println(n1 - n2)

    // Operador de Divisão
    println(n1 / n2)

    // Operador de Multiplicação
    println(n1 * n2)

    // Módulo - Resto da Divisão
    println(n1 % n2)

    // Incremento
    println(++n1)

    // Decrementto
    println(--n2)
}