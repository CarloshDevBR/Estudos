package firstSteps/*
    == Operador de Igual
    < Operador de Menor
    > Operador de Maior
    <= Operador de Menor ou Igual
    >= Operador de Maior ou Igual
    != Operador de Diferente
*/

fun main() {
    val n1: Byte = 20
    val n2: Byte = 18

    // == Operador de Igual
    println(n1 == n2)

    // < Operador de Menor
    println(n1 < n2)

    // > Operador de Menor
    println(n1 > n2)

    // <= Operador de Menor ou Igual
    println(n1 <= n2)

    // >= Operador de Maior ou Igual
    println(n1 >= n2)

    // != Operador de Diferente
    println(n1 != n2)
}
